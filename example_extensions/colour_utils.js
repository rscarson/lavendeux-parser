/**
 * This function tells Lavendeux about this extension.
 * It must return an object similar to the one below.
 * @returns Object
 */
function extension() {
    return {
        name: "HTML Colour Utilities",
        author: "@rscarson",
        version: "0.2.0",

        functions: {
            "complement": "function_complement",
            "color": "function_colour", "colour": "function_colour"
        },

        decorators: {
            "color": "decorator_colour", "colour": "decorator_colour"
        }
    };
}

/**
 * Function that finds an HTML color code for a color with a given name
 *  Usage: color(<string>)
 * Can be called from the lavendeux parser
 * It takes in an array of value objects, which will have one of the following properties:
 *  Integer, Float, String
 * 
 * It then returns a value object
 * @param {Value} args 
 * @returns {Value} result
 */
function function_colour(args) {
    if (args.length != 1) {
        throw "color(s): expected 1 argument";
    } else if (!args[0].String) {
        throw "color(s): expected a string value";
    }

    let result = color_map[args[0].String.toLowerCase()];

    if (result) {
        return {
            "Integer": result
        };
    }

    throw "color(s): unknown color name "+args[0].String.toLowerCase();
}

/**
 * Function that calculates the complement of an integer value representing an HTML color code
 *  Usage: complement(<int>)
 * Can be called from the lavendeux parser
 * It takes in an array of value objects, which will have one of the following properties:
 *  Integer, Float, String
 * 
 * It then returns a value object
 * @param {Value} args 
 * @returns {Value} result
 */
function function_complement(args) {    
    if (args.length != 1) {
        throw "complement(n): expected 1 argument";
    } else if (!args[0].Integer) {
        throw "complement(n): expected an integer value";
    }
    
  // Get RBG bytes, and complement
  let rgb_val = extract_rgb(args[0].Integer);
    rgb_val.r = 255 - rgb_val.r;
    rgb_val.g = 255 - rgb_val.g;
    rgb_val.b = 255 - rgb_val.b;

    return {
        "Integer": pack_rgb(rgb_val)
    };
}

/**
 * Decorator that expresses an integer value into as HTML color code
 *  Usage: <int> @color
 * Can be called from the lavendeux parser
 * It takes in a value objects, which will have one of the following properties:
 *  Integer, Float, String
 * 
 * It then returns a string
 * @param {Value} args 
 * @returns {String} result
 */
function decorator_colour(value) {
    if (value.Integer) {
        return '#'+value.Integer.toString(16).padEnd(6, '0');
    }

    throw "@color: expected an integer value";
}

function extract_rgb(int_val) {
    return {
        "b": int_val & 0xFF,
        "g": (int_val >> 8) & 0xFF,
        "r": (int_val >> 16) & 0xFF
    };
}

function pack_rgb(rgb_val) {
    let val = 0;
    val |= rgb_val.r;
    val = val << 8;
    val |= rgb_val.b;
    val = val << 8;
    val |= rgb_val.b;
    return val;
}

